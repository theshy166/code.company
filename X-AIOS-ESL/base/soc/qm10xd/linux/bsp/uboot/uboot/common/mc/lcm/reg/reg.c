uint32_t s_reg_log = 0x00;
/**---------------------------------------------------------------------------*
 **                 Constant Variables                    *
 **---------------------------------------------------------------------------*/

/**---------------------------------------------------------------------------*
 **                 Local Function Prototypes                *
 **---------------------------------------------------------------------------*/
DATA regRead32(ADDR addr)
{
    DATA data = 0x00;

    data = *(volatile ADDR*)(addr);

    if (1 == s_reg_log) {
        printf("--read  0x%08x, 0x%08x\n", addr, data);
    }

    return data;
}

void regWrite32(ADDR addr, DATA data)
{
    *(volatile ADDR*)(addr) = data;

    if (1 == s_reg_log) {
        printf("--write 0x%08x, 0x%08x\n", addr, data);
    }
}

void regWriteBits32(ADDR addr, uint32_t startBit, DATA mask, DATA data)
{
    DATA rd_data = 0x00;

    rd_data = regRead32(addr);

    rd_data = rd_data & (~(mask << startBit));
    rd_data = rd_data | (data << startBit);

    regWrite32(addr, rd_data);

    if (1 == s_reg_log) {
        printf("--write 0x%08x, 0x%08x\n", addr, rd_data);
    }
}

void reg_log_enable(uint32_t enable)
{
    s_reg_log = enable;
}

